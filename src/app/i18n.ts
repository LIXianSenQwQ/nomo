export const t = new Proxy(generatedMessages, {
  get(target, prop, receiver) {
    if (typeof prop !== 'string') {
      return Reflect.get(target, prop, receiver);
    }

    return (inputs?: Record<string, unknown>) => {
      const extra = getExtraTranslation(prop, currentInterfaceLocale, inputs);
      if (extra !== null) {
        return extra;
      }

      const message = Reflect.get(target, prop, receiver);
      if (typeof message === 'function') {
        return message(inputs);
      }

      return prop;
    };
  },
}) as MessageBag;

export const interfaceLocaleStore = writable<EffectiveInterfaceLocale>(currentInterfaceLocale);

export function getInterfaceLocale(): EffectiveInterfaceLocale {
  return getLocale() as EffectiveInterfaceLocale;
}

export function isInterfaceLanguagePreference(
  value: unknown,
): value is InterfaceLanguagePreference {
  return value === 'system' || isEffectiveInterfaceLocale(value);
}

export function isEffectiveInterfaceLocale(value: unknown): value is EffectiveInterfaceLocale {
  return (
    typeof value === 'string' &&
    SUPPORTED_INTERFACE_LOCALES.includes(value as EffectiveInterfaceLocale)
  );
}

export function getBrowserLanguageCandidates(): string[] {
  if (typeof navigator === 'undefined') {
    return [];
  }

  const languages = Array.isArray(navigator.languages) ? navigator.languages : [];
  return [...languages, navigator.language].filter(
    (language): language is string => typeof language === 'string' && language.length > 0,
  );
}

export function resolveInterfaceLocale(
  preference: InterfaceLanguagePreference,
  languages: readonly string[] = getBrowserLanguageCandidates(),
): EffectiveInterfaceLocale {
  if (preference !== 'system') {
    return preference;
  }

  for (const language of languages) {
    const normalized = language.trim().toLowerCase().replace('_', '-');
    if (!normalized) continue;

    const exactLocale = SUPPORTED_INTERFACE_LOCALES.find(
      (locale) => locale.toLowerCase() === normalized,
    );
    if (exactLocale) {
      return exactLocale;
    }

    if (
      normalized.startsWith('zh-hant') ||
      normalized === 'zh-tw' ||
      normalized.startsWith('zh-tw-') ||
      normalized === 'zh-hk' ||
      normalized.startsWith('zh-hk-') ||
      normalized === 'zh-mo' ||
      normalized.startsWith('zh-mo-')
    ) {
      return 'zh-TW';
    }

    if (
      normalized.startsWith('zh-hans') ||
      normalized === 'zh' ||
      normalized === 'zh-cn' ||
      normalized.startsWith('zh-cn-') ||
      normalized === 'zh-sg' ||
      normalized.startsWith('zh-sg-')
    ) {
      return 'zh-CN';
    }

    const languagePrefix = normalized.split('-')[0];
    const prefixLocale = SUPPORTED_INTERFACE_LOCALES.find((locale) =>
      locale.toLowerCase().startsWith(`${languagePrefix}-`),
    );
    if (prefixLocale) {
      return prefixLocale;
    }
  }

  return DEFAULT_EFFECTIVE_INTERFACE_LOCALE;
}

export function applyInterfaceLanguagePreference(
  preference: InterfaceLanguagePreference,
  languages: readonly string[] = getBrowserLanguageCandidates(),
): EffectiveInterfaceLocale {
  const locale = resolveInterfaceLocale(preference, languages);
  currentInterfaceLocale = locale;
  interfaceLocaleStore.set(locale);

  if (typeof document !== 'undefined') {
    document.documentElement.lang = locale;
    window.dispatchEvent(new CustomEvent(INTERFACE_LOCALE_CHANGED_EVENT, { detail: { locale } }));
  }

  return locale;
}

export function onInterfaceLocaleChanged(callback: () => void): () => void {
  if (typeof window === 'undefined') {
    return () => undefined;
  }

  const listener = () => callback();
  window.addEventListener(INTERFACE_LOCALE_CHANGED_EVENT, listener);
  return () => window.removeEventListener(INTERFACE_LOCALE_CHANGED_EVENT, listener);
}

export function getDiagramTypeLabel(type: string): string {
  const labelKeys: Record<string, string> = {
    flowchart: 'diagramFlowchart',
    sequenceDiagram: 'diagramSequence',
    classDiagram: 'diagramClass',
    stateDiagram: 'diagramState',
    pie: 'diagramPie',
    gantt: 'diagramGantt',
    erDiagram: 'diagramEr',
  };
  const labelKey = labelKeys[type];
  return labelKey ? t[labelKey]() : type;
}

function getExtraTranslation(
  key: string,
  locale: EffectiveInterfaceLocale,
  inputs?: Record<string, unknown>,
): string | null {
  const value =
    EXTRA_TRANSLATIONS[locale]?.[key] ??
    EXTRA_TRANSLATIONS['en-US']?.[key] ??
    EXTRA_TRANSLATIONS['zh-CN']?.[key];
  if (typeof value !== 'string') {
    return null;
  }

  return interpolate(value, inputs);
}

function interpolate(template: string, inputs?: Record<string, unknown>): string {
  if (!inputs) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_match, key: string) => String(inputs[key] ?? ''));
}
